    private void issueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_issueActionPerformed
        try {
            // TODO add your handling code here:
            userID = SIDf.getText();
            System.out.println("1");
            ps2 = con.prepareStatement("SELECT * from `userinfo` WHERE `ID`=?");
            ps2.setString(1, userID);
            rs = ps2.executeQuery();
            System.out.println("2");
            if (rs.next()) {
                System.out.println("3");
                ps2 = con.prepareStatement("SELECT * FROM `bookinfo` WHERE `Book_Name`=? AND `Author_Name`=?");
                ps2.setString(1, bookName.getText());
                ps2.setString(2, authName.getText());
                rs2 = ps2.executeQuery();
                System.out.println("4");
                if (rs2.next()) {
                    System.out.println("5");
                    bookID = rs2.getString("Book_ID");
                }
                try {
                    ps2 = con.prepareCall("INSERT INTO `issueinfo`(`Book_ID`, `Student_ID`) VALUES (?,?)");
                    ps2.setString(1, bookID);
                    ps2.setString(2, SIDf.getText());
                    ps2.executeUpdate();
                    System.out.println("6");
                    JOptionPane.showMessageDialog(null, "Book " + bookID + " issued succesfully.");
                    SIDf.setText(null);
                    bookName.setText(null);
                    authName.setText(null);
                } catch (SQLException ex) {
                    //Logger.getLogger(IssueReturnBooks.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "You have already been issued this book. Please return it first.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Student ID " + userID + " is not registered!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(IssueReturnBooks.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_issueActionPerformed

