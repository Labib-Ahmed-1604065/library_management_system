    public userPanel() {
        initComponents();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "");
            //JOptionPane.showMessageDialog(null, "Connected");
            
        } catch (SQLException ex) {
            Logger.getLogger(addLibrarian.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Not Connected");
        }
        
        try {
            ps=con.prepareStatement("SELECT * FROM userinfo WHERE ID=?");
            ps.setString(1,UserID);
            rs=ps.executeQuery();
            if(rs.next())
            {
                SIDf.setText(UserID);
                namef.setText(rs.getString("Name"));
                mobilef.setText(rs.getString("Mobile"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(userPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        try{
            ps=con.prepareStatement("SELECT * FROM `bookinfo` WHERE Book_ID in (SELECT Book_ID FROM issueinfo WHERE Student_ID=?)");
            ps.setString(1, UserID);
            rs=ps.executeQuery();
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString("Book_Name"),rs.getString("Author_Name")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(userPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

