    public void addData(String studentID,String Name,String Mobile,String password)
    {
        
        try {
            PreparedStatement statement = (PreparedStatement) con.prepareStatement("INSERT INTO `userinfo`(`ID`, `Name`, `Mobile`, `Password`) VALUES (?,?,?,?)");
            
            statement.setString(1, studentID);
            statement.setString(2, Name);
            statement.setString(3, Mobile);
            statement.setString(4, password);
            statement.executeUpdate();
            statement.close();
            System.out.println("User Added");
            JOptionPane.showMessageDialog(null, "Signup succeeded");
            ULogin ul=new ULogin();
            ul.setVisible(true);
            this.dispose();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
