    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try {
            // TODO add your handling code here:
            statement = con.prepareStatement("UPDATE `librarianinfo` SET `Name`=?,`Mobile`=?,`Password`=? WHERE `ID`=?");
            statement.setString(1, viewNameField.getText());
            statement.setString(2, viewMobNumField.getText());
            statement.setString(3, viewPassField.getText());
            statement.setString(4, JL.getSelectedValue().toString());
            statement.executeUpdate();
            editMessage.setText("");
            save.setEnabled(false);
            edit.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Saved");
        } catch (SQLException ex) {
            Logger.getLogger(viewLibrarian.class.getName()).log(Level.SEVERE, null, ex);
        }
        viewNameField.setEditable(false);
        viewMobNumField.setEditable(false);
        viewPassField.setEditable(false);
    }//GEN-LAST:event_saveActionPerformed

